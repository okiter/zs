<script>
$(function(){
	//点击列表进行活动详情显示操作
	//>>遍历活动列表
	$('.activityList').each(function(){
		$(this).click(function(){
			var listId = $(this).attr('data-activitId'); //活动列表的活动id
				contnenId = $(this).find('.content').attr('data-activitId'); //活动详情的活动id
			//>> >>判断活动列表id与活动详情id是否相等
			if(listId==contnenId){
				$(this).css('background','#e5e5e5');
				$(this).find('.content').slideDown('slow');
				$(this).prevAll().find('.content').slideUp('slow');
				$(this).nextAll().find('.content').slideUp('slow');
				$(this).prevAll().removeAttr('style');
				$(this).nextAll().removeAttr('style');
			}
		});
	});
	
	//点击焦点图进行活动详情显示操作
	//>>遍历焦点图链接
	$('.activityFocus a').each(function(){
		$(this).click(function(){
			var linkId = $(this).attr('data-activitId'); //焦点图链接上的活动id
			//>> >>遍历活动列表
			$('.activityList').each(function(){
				var listId = $(this).attr('data-activitId');
					contnenId = $(this).find('.content').attr('data-activitId');
				//>> >> >>判断链接id、列表id、详情id是否相等
				if(linkId==listId && listId==contnenId){
					$(this).css('background','#e5e5e5');
					$(this).find('.content').slideDown('slow');
					$(this).prevAll().find('.content').slideUp('slow');
					$(this).nextAll().find('.content').slideUp('slow');
					$(this).prevAll().removeAttr('style');
					$(this).nextAll().removeAttr('style');
				}
			});
		});
	});
});
